//Program to add 2 numbers
#include <stdio.h>

int main() {
    // Declare and initialize two integer variables
    int num1 = 15; // First number
    int num2 = 25; // Second number
    int sum;       // Variable to store the sum

    // Calculate the sum
    sum = num1 + num2;

    // Print the result
    printf("The sum of %d and %d is %d\n", num1, num2, sum); //%d is a format specifier

    return 0;
}